# todo 1-1 整型（`int`）
'''
对于任意进制数 xyz... 转十进制：
确定进制基数 B（如二进制 B=2，八进制 B=8，十六进制 B=16）。
将每一位数字乘以其对应的权值 B^n，然后相加
'''
print(0b100)  # 二进制整数 todo “0b” [二进制（Binary）] 代表2进制 (1 * 2^2) + (0 * 2^1) + (0 * 2^0)= 4 + 0 + 0 = 4
print(0o100)  # 八进制整数 todo “0o” [八进制（Octal）] 代表8进制 (1 * 8^2) + (0 * 8^1) + (0 * 8^0)= 64 + 0 + 0 = 64
print(100)  # 十进制整数
print(0x100)  # 十六进制整数  todo “0x” [十六进制（Hexadecimal）] 代表16进制 (1 * 16^2) + (0 * 16^1) + (0 * 16^0)= 256 + 0 + 0 = 256

